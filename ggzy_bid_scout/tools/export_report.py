"""
export_report.py — 结构化数据导出与报告生成工具

功能：
  1. 从 data/structured/{name}.json 读取采集数据
  2. 导出 CSV、HTML、JSON 三种格式
  3. 输出到 data/reports/

CSV 字段：title, date, issuer, region, budget, detail_url
"""

from __future__ import annotations

import argparse
import csv
import html
import json
import logging
import sys
from datetime import datetime
from pathlib import Path
from typing import Any

